public class HelloWorld{
        public static void main(String[] args){
            System.out.println("hello world");
    }
}
/*
public: 메서드 접근제어자, public => 누구나 접근 가능함
static: 이 메서드는 인스턴스 생성 없이 실행 가능
void: 메서드의 return 값 없음
String[]: 문자열을 나타내는 자바의 자료형 :[] 여러개의 값으로 이루어진 배열
args: String[]자료형에 대한 변수명
System.out.println: 자바의 내장 메서드로 문자열을 화면에 출력한다.
*/
